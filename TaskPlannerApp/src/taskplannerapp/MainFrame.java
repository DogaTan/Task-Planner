    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            // TODO add your handling code here:
            Connection conn = TaskPlanner_Database.myConnection();
    
            Statement statement2 =(Statement) TaskPlanner_Database.myConnection().createStatement();
            ResultSet rs = statement2.executeQuery("SELECT User_Id FROM users WHERE Username ='"+ usernameLabel.getText() +"' ");
            String userID = null;
            if(rs.next()){
               userID = rs.getString(1);  
            }
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String deadline = sdf.format(deadlineChooser.getDate());
            
       
            String query = "INSERT INTO tasks(Task_Name,Short_Desc,Deadline,Priority,Reminder_Image,User_Id) VALUES(?,?,?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1,taskNameField.getText());
            pst.setString(2,shortDescField.getText() );
            pst.setString(3,deadline );
            pst.setString(4,priorityField.getText() );
            pst.setBoolean(5,reminderImageCheck.isSelected());
            pst.setString(6, userID);
            JOptionPane.showMessageDialog(null, "Record inserted...");  
            pst.executeUpdate();
            Refresh();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e); 
        }
        
        
    }//GEN-LAST:event_saveButtonActionPerformed

